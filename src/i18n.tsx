interface I18nContextType {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  td: (value: string) => string;
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem('lang') as Lang | null;
    return saved === 'ar' || saved === 'en' || saved === 'tr' ? saved : 'ar';
  });

  useEffect(() => {
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = (l: Lang) => setLangState(l);

  const t = (key: string, params?: Record<string, string | number>) => {
    let s = translations[lang][key] ?? translations.ar[key] ?? key;
    if (params) {
      Object.keys(params).forEach((k) => {
        s = s.replace(`{${k}}`, String(params[k]));
      });
    }
    return s;
  };

  const td = (value: string) => {
    if (lang === 'ar') return value;
    const entry = dataDict[value];
    return entry ? entry[lang] : value;
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t, td }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = (): I18nContextType => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within LanguageProvider');
  return ctx;
};

export const LanguageSwitcher: React.FC<{ className?: string }> = ({ className = '' }) => {
  const { lang, setLang } = useI18n();
  const langs: { code: Lang; label: string }[] = [
    { code: 'ar', label: 'عربي' },
    { code: 'en', label: 'EN' },
    { code: 'tr', label: 'TR' },
  ];
  return (
    <div className={`flex items-center gap-1 bg-black/20 rounded-lg p-1 ${className}`}>
      {langs.map((l) => (
        <button
          key={l.code}
          onClick={() => setLang(l.code)}
          className={`px-2.5 py-1 rounded-md text-xs font-bold transition-all ${
            lang === l.code
              ? 'bg-white text-slate-900 shadow'
              : 'text-white hover:bg-white/20'
          }`}
        >
          {l.label}
        </button>
      ))}
    </div>
  );
};
